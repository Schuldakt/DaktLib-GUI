struct Font::Impl
{
    std::vector<u8> fontData;  // Keep TTF data alive
};

Font::Font() : m_impl(std::make_unique<Impl>()) {}
Font::~Font() = default;

bool Font::loadFromFile(StringView path, const FontConfig& config)
{
    auto result = dakt::core::fs::readFile(path);
    if (!result)
    {
        return false;
    }

    // Convert from core::byte to u8
    auto& buffer = *result;
    m_impl->fontData.resize(buffer.size());
    std::memcpy(m_impl->fontData.data(), buffer.data(), buffer.size());
    return loadFromMemory(m_impl->fontData, config);
}

bool Font::loadFromMemory(std::span<const u8> data, const FontConfig& config)
{
    // TODO: Implement TrueType parsing with stb_truetype or similar
    // For now, fall back to default
    (void)data;
    (void)config;
    return loadDefault(config.sizePixels);
}

bool Font::loadDefault(f32 sizePixels)
{
    using namespace builtin_font;

    m_fontSize = sizePixels;
    f32 scale = sizePixels / static_cast<f32>(GLYPH_HEIGHT);
    m_scale = scale;

    // Set font metrics
    m_ascent = 10.0f * scale;
    m_descent = 3.0f * scale;
    m_lineHeight = static_cast<f32>(GLYPH_HEIGHT) * scale;

    // Build atlas (all characters in a row)
    i32 atlasWidth = GLYPH_WIDTH * CHAR_COUNT;
    i32 atlasHeight = GLYPH_HEIGHT;

    if (m_atlas)
    {
        m_atlas->m_width = atlasWidth;
        m_atlas->m_height = atlasHeight;
        m_atlas->m_pixels.resize(atlasWidth * atlasHeight);
        std::fill(m_atlas->m_pixels.begin(), m_atlas->m_pixels.end(), static_cast<u8>(0));

        // Rasterize each character
        for (i32 c = 0; c < CHAR_COUNT; ++c)
        {
            i32 xOffset = c * GLYPH_WIDTH;

            for (i32 y = 0; y < GLYPH_HEIGHT; ++y)
            {
                u8 row = fontData[c][y];
                for (i32 x = 0; x < GLYPH_WIDTH; ++x)
                {
                    bool bit = (row & (0x80 >> x)) != 0;
                    m_atlas->m_pixels[y * atlasWidth + xOffset + x] = bit ? 255 : 0;
                }
            }
        }

        m_atlas->m_dirty = true;
    }

    // Build glyph map
    m_glyphs.clear();

    for (i32 c = 0; c < CHAR_COUNT; ++c)
    {
        u32 codepoint = FIRST_CHAR + c;

        GlyphInfo glyph;
        glyph.codepoint = codepoint;
        glyph.metrics.advance = static_cast<f32>(GLYPH_WIDTH) * scale;
        glyph.metrics.leftBearing = 0.0f;
        glyph.metrics.width = static_cast<f32>(GLYPH_WIDTH) * scale;
        glyph.metrics.height = static_cast<f32>(GLYPH_HEIGHT) * scale;
        glyph.metrics.offsetX = 0.0f;
        glyph.metrics.offsetY = 0.0f;

        f32 u0 = static_cast<f32>(c * GLYPH_WIDTH) / static_cast<f32>(atlasWidth);
        f32 u1 = static_cast<f32>((c + 1) * GLYPH_WIDTH) / static_cast<f32>(atlasWidth);
        glyph.uvMin = {u0, 0.0f};
        glyph.uvMax = {u1, 1.0f};
        glyph.atlasPage = 0;

        m_glyphs[codepoint] = glyph;
    }

    // Set fallback glyph (use '?')
    auto it = m_glyphs.find('?');
    if (it != m_glyphs.end())
    {
        m_fallbackGlyph = it->second;
    }

    m_loaded = true;
    return true;
}

const GlyphInfo* Font::findGlyph(u32 codepoint) const
{
    auto it = m_glyphs.find(codepoint);
    if (it != m_glyphs.end())
    {
        return &it->second;
    }
    return nullptr;
}

f32 Font::getKerning(u32 left, u32 right) const
{
    u64 key = (static_cast<u64>(left) << 32) | right;
    auto it = m_kerningPairs.find(key);
    return it != m_kerningPairs.end() ? it->second : 0.0f;
}

Vec2 Font::calcTextSize(StringView text, f32 wrapWidth) const
{
    if (text.empty() || !m_loaded)
        return {0, 0};

    f32 width = 0.0f;
    f32 maxWidth = 0.0f;
    f32 height = m_lineHeight;

    u32 prevCodepoint = 0;

    for (char ch : text)
    {
        u32 codepoint = static_cast<u32>(static_cast<u8>(ch));

        if (ch == '\n')
        {
            maxWidth = std::max(maxWidth, width);
            width = 0.0f;
            height += m_lineHeight;
            prevCodepoint = 0;
            continue;
        }

        const GlyphInfo* glyph = findGlyph(codepoint);
        if (!glyph)
            glyph = &m_fallbackGlyph;

        if (prevCodepoint)
        {
            width += getKerning(prevCodepoint, codepoint);
        }

        width += glyph->metrics.advance;

        // Word wrap
        if (wrapWidth > 0.0f && width > wrapWidth && ch == ' ')
        {
            maxWidth = std::max(maxWidth, width - glyph->metrics.advance);
            width = 0.0f;
            height += m_lineHeight;
        }

        prevCodepoint = codepoint;
    }

    maxWidth = std::max(maxWidth, width);
    return {maxWidth, height};
}

f32 Font::calcTextWidth(StringView text) const
{
    return calcTextSize(text, 0.0f).x;
}

usize Font::calcTextWrapPosition(StringView text, f32 wrapWidth) const
{
    if (text.empty() || wrapWidth <= 0.0f)
        return text.size();

    f32 width = 0.0f;
    usize lastSpace = 0;
    u32 prevCodepoint = 0;

    for (usize i = 0; i < text.size(); ++i)
    {
        char ch = text[i];
        u32 codepoint = static_cast<u32>(static_cast<u8>(ch));

        if (ch == ' ')
            lastSpace = i;
        if (ch == '\n')
            return i;

        const GlyphInfo* glyph = findGlyph(codepoint);
        if (!glyph)
            glyph = &m_fallbackGlyph;

        if (prevCodepoint)
        {
            width += getKerning(prevCodepoint, codepoint);
        }

        width += glyph->metrics.advance;

        if (width > wrapWidth)
        {
            return lastSpace > 0 ? lastSpace : i;
        }

        prevCodepoint = codepoint;
    }

    return text.size();
}

void Font::renderText(DrawList& drawList, Vec2 pos, StringView text, Color color, f32 clipMaxX) const
{
    if (text.empty() || !m_loaded || !m_atlas)
        return;

    f32 x = pos.x;
    f32 y = pos.y;
    u32 prevCodepoint = 0;

    for (char ch : text)
    {
        u32 codepoint = static_cast<u32>(static_cast<u8>(ch));

        if (ch == '\n')
        {
            x = pos.x;
            y += m_lineHeight;
            prevCodepoint = 0;
            continue;
        }

        const GlyphInfo* glyph = findGlyph(codepoint);
        if (!glyph)
            glyph = &m_fallbackGlyph;

        if (prevCodepoint)
        {
            x += getKerning(prevCodepoint, codepoint);
        }

        // Clip check
        if (clipMaxX > 0.0f && x > clipMaxX)
            break;

        // Draw glyph quad
        Vec2 p0 = {x + glyph->metrics.offsetX, y + glyph->metrics.offsetY};
        Vec2 p1 = p0 + Vec2{glyph->metrics.width, glyph->metrics.height};

        drawList.addImage(m_atlas->textureId(), Rect{p0, p1}, glyph->uvMin, glyph->uvMax, color);

        x += glyph->metrics.advance;
        prevCodepoint = codepoint;
    }
}

