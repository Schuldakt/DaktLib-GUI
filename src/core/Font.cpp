FontAtlas::FontAtlas() = default;
FontAtlas::~FontAtlas() = default;

void FontAtlas::clear()
{
    m_pixels.clear();
    m_width = 0;
    m_height = 0;
    m_textureId = 0;
    m_dirty = true;
    m_customRects.clear();
    m_nextCustomRectId = 1;
}

bool FontAtlas::build()
{
    // Atlas already built
    if (!m_dirty && !m_pixels.empty())
    {
        return true;
    }

    m_dirty = false;
    return true;
}

u32 FontAtlas::addCustomRect(i32 width, i32 height)
{
    CustomRect rect;
    rect.id = m_nextCustomRectId++;
    rect.width = width;
    rect.height = height;
    m_customRects.push_back(rect);
    m_dirty = true;
    return rect.id;
}

const FontAtlas::CustomRect* FontAtlas::getCustomRect(u32 id) const
{
    for (const auto& rect : m_customRects)
    {
        if (rect.id == id)
            return &rect;
    }
    return nullptr;
}

