Font* FontManager::addFont(StringView path, f32 sizePixels, const FontConfig& config)
{
    auto font = std::make_unique<Font>();
    font->setAtlas(&m_atlas);

    FontConfig cfg = config;
    cfg.sizePixels = sizePixels;

    if (!font->loadFromFile(path, cfg))
    {
        return nullptr;
    }

    Font* ptr = font.get();
    m_fonts.push_back(std::move(font));

    if (!m_defaultFont)
    {
        m_defaultFont = ptr;
    }

    m_atlas.setDirty();
    return ptr;
}

